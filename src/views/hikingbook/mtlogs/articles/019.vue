        <p>但五星級終究也是比較出來的，位處深山，高山山莊到底仍難以做到像平地飯店那般盡善盡美，除了不提供盥洗，電力供應時間也有限制。雖說是東西電力輸送的樞紐，天池山莊的電力卻全仰賴太陽能，很難支應全天候運作，因此過了晚上八點後除了大廳之外的房間電燈都會強制關閉。</p>
