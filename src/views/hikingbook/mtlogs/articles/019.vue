        <p>從下午開始降下的雨直到入夜都不見一絲停歇跡象，在大廳吃晚餐的時候，包含我們在內，四處都聽得到其他隊伍議論著明天如果雨還是沒停，是否要取消攻頂奇萊南華的行程？有山友直接向人也在大廳的莊主提問，只見他一語不發默默地走到門口，像是卜卦又像是在配合演出似地抬頭瞧了瞧天空，然後斬釘截鐵地說「晚點雨就不會再下了」、「明天會出大景，要看日出的早點睡」等豪語，不曉得這份自信是憑藉他長年居住在天池山莊的經驗，還是純粹要安撫大家懸吊著的不安心情，但事實證明，一切都如莊主所料。</p>
