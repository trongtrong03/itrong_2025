        <p>我們被分配到的床位是八人的大通鋪，沒有床墊和棉被，僅透過巧拼鋪在木板上方增加一些軟適感，至於棉被，可以向山莊租借睡袋，而我們則使用自行攜帶的睡袋。簡陋歸簡陋，但在這深山老林之中還有這樣的住宿環境，倒也沒什麼好苛求的，而且房間打掃得很乾淨，內部幾乎看不到灰塵聚集與蜘蛛網叢生。</p>
        <p>天池山莊還有其他高山山屋很難看見的「現代廁所」，想起隔年去爬南湖群峰，平平都是熱門路線，南湖山屋的廁所相比顯得非常「原汁原味」，大概十步之外就得捏著鼻子才能靠近，小解當下就立刻回憶起天池山莊的美好，不僅有小便斗和蹲式馬桶，還附帶自動沖水功能。</p>
