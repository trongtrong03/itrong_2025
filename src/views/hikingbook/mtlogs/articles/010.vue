        <p>五指山不只在漢人眼裡是非常重要的開拓據點，對原本就生活在寶島土地的原住民更是如此。定居於五指山周遭的原住民族群主要為泰雅族與賽夏族，一如其他地域發展史，當漢人與原住民接觸時，無可避免地就會爆發衝突，雙方互有死傷。到了日治時期，日不落帝國派軍前來接收台灣，漢人、原住民、日本人三方之間的關係又演化地更加複雜。初期日本人為了盡速弭平漢人反抗勢力，便與原住民交好，一方面是想藉助原住民的協助剿滅漢人，一方面是避免腹背受敵；而對原本與漢人就有仇恨的原住民而言，日本人釋出的善意讓他們認為日本人和漢人是不一樣的，是友好可以親近的民族，便大力協助日軍驅逐漢人。然而隨著反日勢力逐漸黯淡，開墾需求已重於撫卹，定居山林的原住民反而成為了日本人的眼中釘，曾經承諾日本人不會進入蕃地的承諾，隨著士兵和火炮的進駐，成為砲火中的一縷硝煙。</p>
