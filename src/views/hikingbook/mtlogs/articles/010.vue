        <h3>二、指峰凌霄</h3>
        <p>五指山，顧名思義就是山形由五座山峰相連，宛若人類的五根手指，五指山一名早在清朝時期便以出現，當時《淡水廳志》就以「五指連雲」列入塹南八景之一，顯現五指山在彼時便以名聲顯赫。除此之外，五指山於地理層面也是新竹重要的參考地點，根據鄭用錫《淡水廳志稿》〈山川篇〉的描述，竹塹城址有五指山為「祖山」，其龍脈由五指山中指逶迤西行至金山面，再西下延粟湖至十八尖山，金山面為「少祖山」，十八尖山為「父母山」。</p>
        <p>以五指山為交界的北埔鄉，有個「金廣福」這個名聲響亮的國定古蹟，金廣福指的是早前開墾北埔與大隘地區一帶，以姜家為首成立的拓墾組織。據說姜家在開墾時就曾借鑑五指山的風水地理「龍」、「穴」、「砂」、「水」，格局十分完整，以此定下金廣福墾號對該地區重大影響力的基礎，由此可見五指山地理位置的重要性。</p>
