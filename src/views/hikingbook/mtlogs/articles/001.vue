        <p>猶記得距今十年前的大學時期，也恰好是我們三人，暑假一到便騎著野狼去環島，出發前得知有颱風要掠過南臺灣，但我們年輕氣盛，並未因此取消計畫，而是決意和颱風拚個你所我活......當然這樣講是有些過於誇大，其實一方面也是因為氣象局認為這個颱風是個擦邊球，移動速度又快，估計為台灣帶來的影響不會太長，所以我們便想，只要撐過頭一、兩天的南部行程，到東部即可平步青雲，就結果來說也一如氣象局預期，颱風影響台灣天氣的時間很短。</p>
        <p>然而，錯就錯在我們低估其挾帶的雨勢，颱風是走的很瀟灑，卻甩了一大盆的雨給南台灣，我們騎在通往恆春唯一要道的台一線上，不斷地被車輛疾駛而過所濺起的水花洗臉，與其說是水花，不如說幾乎是一道道水浪，才甫被潑滿一身，下一道水浪又隨即撲打上來，我們三人三狼，特是狼狽。</p>
