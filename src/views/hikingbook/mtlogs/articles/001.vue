            <img src="/images/mtlogs/001/34.jpg">
        </figure>
        <p>固然有風險，這方還是順利於下午三點安全返抵登山口，但是當撞見一輛救護車停在登山口正前方，心頭不由得驚了一晌，趕緊傳訊息關心友人。叮咚一聲，傳出去的訊息不一會兒就收到回覆，訊息另一端的兩位事主表示自己正安然無恙地在孟祿亭休息，等菸抽完就會繼續上路，聽完這番話頭頂立刻畫下三條線，這邊開ＴＵＲＢＯ渦輪加速快馬下山，結果你們還好整以暇在上面哈菸......但轉念想想，至少確定人沒事就好。</p>
        <p>後來詢問了一下一旁待命的員警，他說有山友在往玉山前峰的石瀑上，不知道是頭去撞到還是被石頭砸到頭，反正就是頭部受創，接獲通報所以緊急過來救援，疫情肆虐的這幾年，山難救援案件次數爆增，著實辛苦這些救難人員。員警反問我有沒有遇到救難人員與受傷的山友，由於最後幾公里自己很專注在行走上，並沒有一一留意擦肩而過的山友們，但回想起經過玉山前峰登山口後，山友也確實比較多一些，模糊印象中曾聽到他們自稱是一日玉山前峰往返的健走團，儘管如此，也依然沒有任何遇到傷患的記憶。很抱歉，幫不上忙。</p>
