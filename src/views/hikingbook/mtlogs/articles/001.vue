            <img src="/images/mtlogs/001/35.jpg">
        </figure>
        <p>在那之後一輛接駁車駛了進來，向司機確認每隔半小時就會有一班接駁，最晚發車時間在六點半，以人頭數計價，一人收一百元等資訊後，接下來就只顧著和原住民出身的司機閒聊，沒注意到暢談過程中，救護車已經接走傷患下山去了。冬季尤其是下雪天，然後又是平常日，接駁需求可說少之又少，司機要我先坐到車上等人，會等我的朋友全到齊後再發車，不得不說絕大多數原住民都蘊含著溫柔體貼的內心，所謂「鐵漢柔情」用在他們身上極為貼切。</p>
        <p>一個小時後，兩名友人也風塵僕僕地平安返回登山口，雖說回到家才是真正的平安登出，但此刻我們已按耐不住將圓滿兩個字掛在嘴邊高頌。</p>
        <p>縱然這兩天發生很多突發狀況，很多事情也都不如預期般順利，不過我們三個憑藉一股傻勁，終於成功解鎖了三件視為台灣人人生必須要做的事之一──「攀登玉山」。隨著年紀增長，或許未來我們聚首的頻率間隔會越來越長，但是曾經一起瘋狂過的足跡，肯定會突破南北距離限制，始終串聯著彼此。</p>
