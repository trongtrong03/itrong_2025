            <img src="/images/mtlogs/001/33.jpg">
        </figure>
        <p>距離登山口越來越近，溫度也慢慢提升上來，雖說最高溫度依舊是寒冷的個位數，但神奇的是絲毫都不覺得寒冷，甚至體感還有一點點熱......或許，這是數小時前如夢似幻的絕美經歷，能讓我意識到這一切都是實實在在發生過的僅存證明。</p>
        <p>可是對於膝蓋受傷的友人來說，恐怕就沒這麼多閒情逸致想這些有的沒的了。因為步調放得很緩慢，直至下午兩點鐘時，我們才剛離開西峰觀景台不久，由於三人都沒搭過接駁車，不了解末班車時間以及搭乘規則，對其資訊所知極少。於是後來決定，由腳程比較快的我先行一步，看是否能確保車輛，等待另外兩人下到登山口後，能立即搭接駁車回到上東埔停車場，直接免去三公里的馬路路程，同時也能減輕受傷友人的身心負擔。</p>
        <p>但這種作法也只僅限於像玉山步道這樣難度等級低、安全係數高的路線，且又是特殊狀況才「勉強」可用，不然以登山安全常規裡，「分隊」是極度不安全的作法，甚至可以說是登山一大禁忌。網路隨手一搜山難事件，一半原因可能來自於登山者本身準備不足，另一半原因則往往來自罹難者脫離大部隊而導致。</p>
