        <p>玉山西峰的峰頂幾乎被鐵杉遮覆，展望不佳，但對大多數有做功課的人而言，來西峰也不單只是想蒐集這座百岳就完事那樣簡單而已，玉山西峰最大的特色，在於距離其山頂一百多公尺的山神廟。我們常聽到有人談論全台灣最高的便利超商在哪裡，但若要說到全台灣海拔最高的廟宇，這座位處玉山西峰峰頂西南方的山神廟恐怕是當仁不讓了。該山神廟的前身，乃是日治時期保留下來的「西山神祠」，西山是過去日本人對玉山西峰的稱呼，神祠指的就是日本神社。過去日本政府曾在玉山、雪山等高山設立神社，作為登山者祈求平安之途，但是在二戰結束日本敗撤，國民政府接管台灣後，因皇民化運動在本島廣設的神社幾乎都被拆除，當然也包含了這些高山神社。而我們現在看到的西山神祠，其實是後來玉山國家公園仿日式建築重新建立的廟宇，雖然無法復刻歲月積累遺留的滄桑，卻也重新喚起人們對西峰昔日的點點滴滴。</p>
        <p>說了這麼多，我們在三角點拍完照後並沒有繼續前往西峰神祠！這並非當下我們不曉得這座山神廟，而是因為實在是挺不住寒冷，只想趕快在相機留下自己與西峰的合影然後返程，連短短一百公尺都不想挺進，由此可想而知當下有多寒冷，當然也不排除是我們不耐寒的因素啦。但是如果現在問我們會不會後悔當時沒多堅持那一百公尺......答案顯而易見為「ＹＥＳ」。不過已經過去的事後悔也沒有助益，就當作刻意多留給自己一道下次再訪玉山群峰時的動機。</p>
