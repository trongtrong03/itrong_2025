            <img src="/images/mtlogs/001/15.jpg">
        </figure>
        <p>雖然寫這篇文章當下，排雲山莊已經更換別的經營團隊，供餐也有很大的改變。不過回到２０２１這年，盛裝膳食的容器仍是陶瓷盤子而非現在的鐵盤，晚餐開飯的表定時間為五點，時間一到，山莊工作人員便有如學生時代打飯班，排排站好為飢腸轆轆的登山客打上飯菜，至於味道......就別苛求了吧，稱不上美味，但也絕對不到難吃的地步，考量深山補給不易，對我來說能吃出食物本身的味道，又能吃飽其實已經很滿足了，只要價位不要太誇張就行。但有一說一，甜湯很好喝，最後還裝滿保溫瓶準備留到明天飲用。</p>
        <p>如果無法接受山莊訂餐是自助餐的品質，賣的卻是簡餐的價位，要自己準備食物或背食材上山炊煮也可以，但排雲山莊規劃的自煮區範圍不大，如果同時間自理的隊伍數量多，可能得多等候一些時間，或是盡可能擠出空間，無論如何，用餐品質和花錢向山莊訂餐的人肯定有落差。</p>
        <p>這趟玉山行的首日，在最後一支來自菲律賓的登山隊，入夜後陸續從玉山主峰返回，獲得眾人鼓掌迎接的熱鬧氛圍裡，歡樂地畫下了句點。</p>
