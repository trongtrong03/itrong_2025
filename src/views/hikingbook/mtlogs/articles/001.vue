        <p>可能是暴露在寒冷低溫下太久，又或者前日沒有充分獲得恢復，昨天因為身體不適沒有跟隨我們前往西峰的友人，在剛離開山頂沒多久，便表示自己膝後側疼痛感有越來越顯著的徵兆，儘管看上去還能走路，在下踩石階時卻已可以看到他略感吃力的模樣。然而，此刻我們身上也沒有攜帶藥物，且亦找不到合適的地點停下來幫他進行治療，我們當下能做的也只有放緩步伐，讓他夾在我和另一名友人中間，減少他會因為為了緊著我們隊伍，勉強自己加速行走進而導致傷勢變得更劇烈。總而言之，一切等回到排雲山莊後再做打算，也不知道該不該說是幸好，反正在那之後他的膝蓋一直維持在要爆與不爆之間的微妙狀態，安全地回到排雲山莊。</p>
        <figure>
            <img src="/images/mtlogs/001/31.jpg">
        </figure>
        <p>隨著海拔下降，我們身處的世界顏色又逐漸變得多采多姿起來。在返回排雲山莊過程中，我們還遇到另外兩隊試圖上山的勇士，其中一支多人隊伍看上去裝備精良、經驗豐富，要成功登頂應該不成問題；但另外一組情侶檔恐怕就沒那麼樂觀了，女方臉色幾乎與慘白的山色融為一體，腳步也比一般人步伐來得遲緩......只是看著她無比堅毅的眼神，已經經過洗禮的我們，自然也就理所當然地投以打氣與祝福。</p>
        <p>如果是電影情節，當我們回到排雲山莊時，或許可以沐浴在山屋內所有人英雄式的歡呼聲中吧！但真實情況其實是脫鞋脫冰爪後進入山莊，喝點熱水，休息，然後收拾行李，與莊主道別，一切是如此普普通通。當然，也是有些山友得知我們從主峰回來後，投以此起彼落的欽佩與感嘆，或向我們索要照片讓他們一探主峰峰頂的最新畫面。</p>
        <p>離開山莊前曾發生一段小插曲，那就是我們在大廳要幫膝蓋部位疼痛的友人噴肌樂的時候，恰好被櫃檯的山莊管理員看見，並直言在山上噴這些藥劑沒有效果，反而要友人拉拉筋多休息，並熱心傳授幾招伸展技巧。姑且不論有沒有醫學根據，只是我們今天就得下山了，休息時間有限，需要一些立即見效的應急措施，但是也不好意思當著莊主的面，把他的建議當耳邊風，最後決定先餵友人吃止痛藥看是否能起作用。</p>
        <figure>
            <img src="/images/mtlogs/001/32.jpg">
        </figure>
        <p>也許知道這幾天有寒流，加上是星期一平日，感覺今天要去排雲山莊住宿的登山客比昨天又來的更少了，幾乎都是隔一大段路，才會遇到一組人馬，偶爾摻雜幾名勇敢的獨攀客。在隊伍交會慣性的寒暄裡，最常聽到的除了業界默契的「你好」之外，就屬「上面天氣如何」這句話。明天會不會放大晴我們不知道，但如果明天也是像今天早上所經歷的這番絕美，我想，反而會比尋常大景來得更加觸動人心。</p>
